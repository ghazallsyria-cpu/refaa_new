'use client';

import { QueryClient, QueryClientProvider } from '@tanstack/react-query';
import { useState } from 'react';

// ==========================================
// 📡 مزوّد الاستعلامات المركزي (Query Provider)
// هذا المكون يغلف التطبيق بالكامل ليمنح جميع الصفحات القدرة على استخدام
// خطافات (Hooks) مثل useQuery و useMutation لإدارة البيانات والكاش (Cache).
// ==========================================
export function QueryProvider({ children }: { children: React.ReactNode }) {
  
  // ==========================================
  // ⚙️ تهيئة عميل الاستعلام (QueryClient Initialization)
  // تم استخدام useState مع دالة تهيئة (Lazy Initialization) كنمط (Singleton)
  // لضمان إنشاء الـ QueryClient مرة واحدة فقط طوال دورة حياة التطبيق، 
  // وعدم تدمير الكاش (Cache) مع كل إعادة تصيير (Re-render) في React.
  // ==========================================
  const [queryClient] = useState(
    () =>
      new QueryClient({
        defaultOptions: {
          queries: {
            
            // 🔄 1. فترة الصلاحية (Stale Time)
            // 🚀 الإعداد المعماري: 0 (صفر ملي ثانية)
            // المعنى: البيانات تعتبر "قديمة" فور وصولها. 
            // الفائدة: بمجرد أن يتنقل المعلم من صفحة لأخرى، سيقوم النظام تلقائياً بطلب البيانات الجديدة في الخلفية. 
            // هذا يضمن عدم عرض درجات أو رسائل قديمة تم أخذها من الكاش.
            staleTime: 0, 
            
            // 📥 2. إعادة الطلب عند فتح المكون (Refetch On Mount)
            // 🚀 الإعداد المعماري: true
            // المعنى: إجباري! في كل مرة يتم فيها فتح صفحة أو مكون (Mount)، اطلب البيانات مجدداً.
            // الفائدة: يضمن أن الطالب يرى التحديثات (مثل واجب جديد) فور دخوله لصفحة الواجبات.
            refetchOnMount: true, 
            
            // 🛡️ 3. إعادة الطلب عند التركيز على النافذة (Refetch On Window Focus)
            // 🚀 الإعداد المعماري: false (قرار عبقري لإنقاذ السيرفر!)
            // المعنى: تعطيل الجلب التلقائي عندما يعود المستخدم للتبويبة.
            // الفائدة: المعلم والطالب يفتحون الجوال، يردون على واتساب، ثم يعودون للمنصة مئات المرات يومياً.
            // لو كان هذا الإعداد true، سيتلقى سيرفر Supabase هجوماً عشوائياً (DDoS غير مقصود) مع كل فتح وقفل للشاشة.
            // تعطيله يحمي موارد النظام بشكل هائل ويقلل تكاليف الاستضافة.
            refetchOnWindowFocus: false, 
            
            // ♻️ 4. محاولات إعادة الطلب عند الفشل (Retry Logic)
            // 🚀 الإعداد المعماري: 1
            // المعنى: إذا فشل جلب البيانات (بسبب ضعف الإنترنت مثلاً)، حاول مرة واحدة فقط بدلاً من 3 مرات (الافتراضي).
            // الفائدة: الفشل السريع (Fail Fast). يوفر الباقة للمستخدم، ويظهر له رسالة الخطأ بسرعة بدلاً من تركه معلقاً ينظر لأيقونة التحميل لفترة طويلة.
            retry: 1, 
          },
        },
      })
  );

  return (
    // 🌐 تغليف التطبيق لتوفير السياق (Context) لجميع المكونات الداخلية
    <QueryClientProvider client={queryClient}>
      {children}
    </QueryClientProvider>
  );
}
